void main(List<String> args) {

// SOAL Branching
  var nilai = 90;

  if (nilai > 70) {
    print('Nilai A');
  } else if (nilai > 40) {
    print('Nilai B');
  } else if (nilai > 0) {
    print('Nilai C');
  } else {
    print("Kosong");
  }  

  // SOAL Looping
  for (var i = 1; i < 11; i++) {
    print(i);
  }
}
